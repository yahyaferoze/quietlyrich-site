'use client';

import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { FantasyModeProvider, useFantasyMode } from "./components/FantasyModeContext";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});
const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

// âœ… This component runs on the client and lets us conditionally apply fantasy-mode class
function BodyWithFantasyClass({ children }: { children: React.ReactNode }) {
  const { fantasyMode } = useFantasyMode();
  const bodyClass = `${geistSans.variable} ${geistMono.variable} antialiased bg-black text-white min-h-screen overflow-x-hidden ${
    fantasyMode ? 'fantasy-mode' : ''
  }`;

  return <body className={bodyClass}>{children}</body>;
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <FantasyModeProvider>
        <BodyWithFantasyClass>{children}</BodyWithFantasyClass>
      </FantasyModeProvider>
    </html>
  );
}
